
public class solidRectangle {
    public static void main(String[] args) {
        
        // solid rectangle 

        int n = 4;//for the row
        int m = 5;//for the column
        for(int i=1; i<=n; i++){
            for(int j=1; j<=m; j++){
                System.out.print("* ");
            }
            System.out.println();
        }
    }
}
